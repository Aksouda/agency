    <h2>Ce este un cookie?</h2>
    Incercam sa facem experienta dvs. cat mai simpla si mai utila atunci cand interactionati cu site-ul nostru. Cookie-urile pot fi setate de website-ul pe care il vizitati ("first party cookies") sau pot fi setate de alte website-uri care afișează informații pe pagina pe care o vizitati ("third party cookies").
    <br>

    <h2>Scopul modulelor cookie</h2><br>
    Modulele cookie pot asigura o <span>interacțiune mai rapidă și mai ușoară între utilizatori și website-uri.</span> Spre exemplu, în momentul autentificării unui utilizator pe un anumit website, datele de autentificare sunt stocate într-un modul cookie; ulterior, utilizatorul poate accesa respectivul website fără să fie nevoie să se autentifice din nou.<br>
    În alte cazuri, modulele cookie pot fi utilizate pentru <span>stocarea de informații referitoare la activitățile desfășurate de utilizator pe o anumită pagină web</span>, astfel încât acesta să își poată relua ușor respectivele activități la o accesare ulterioară a website-ului. Modulele cookie spun serverului ce pagini trebuie să arate utilizatorului, astfel încât acesta să nu fie nevoit să își amintească acest lucru sau să navigheze întregul website de la început. Astfel, modulele cookie pot fi asimilate unor „semne de carte” care îi spun utilizatorului exact unde a rămas în cadrul unui website.<br><br>
    
